    private void submit_alunosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submit_alunosMouseClicked
        try {
            Operations operations = new Operations();
            
            String userStr = txtusuario_alunos.getText();
            String passwordStr = txtsenha_alunos.getText();
            
            if(Operations.isLogin(userStr, passwordStr, this)) {
                Operations.getData();
                new Tela_Aluno().setVisible(true);
                LoginSessionAlunos.isLoggedIn = true;
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Usuário e/ou senha incorreto(s)");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Main_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_submit_alunosMouseClicked

    private void submit_admMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submit_admMouseClicked
        try {
            Operations operations = new Operations();
            String userStr = txtusuario_adm.getText();
            String passwordStr = txtsenha_adm.getText();
            if(Operations.isLoginADM(userStr, passwordStr, this)) {
                Operations.getData();
                new PaginaPrincipalAdministrador().setVisible(true);
                LoginSessionAlunos.isLoggedIn = true;
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Usuário e/ou senha incorreto(s)");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Main_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_submit_admMouseClicked

    private void submit_professoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submit_professoresMouseClicked
        try {
            Operations operations = new Operations();
            String userStr = txtusuario_professores.getText();
            String passwordStr = txtsenha_professores.getText();
            if(Operations.isLoginProfessores(userStr, passwordStr, this)) {
                Operations.getDataProfessores();
                new Tela_Professor().setVisible(true);
                LoginSessionProfessores.isLoggedIn = true;
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Usuário e/ou senha incorreto(s)");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Main_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_submit_professoresMouseClicked
      
