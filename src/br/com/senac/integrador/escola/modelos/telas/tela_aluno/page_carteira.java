    private static boolean isSQLSet = false;
    private static Connection connection;

    private static Connection createConnection() throws SQLException {
        if(isSQLSet) {
            return connection;
        }
        //String username = JOptionPane.showInputDialog("Insira o usuário do banco de dados.");
        //String password = JOptionPane.showInputDialog("Insira a senha do banco de dados.");
        
        String username = "root";
        String password = "inserida";
        
        String url = "jdbc:mysql://localhost/appescola";
        isSQLSet = true;
        connection = DriverManager.getConnection(url, username, password);
        return connection;
    }
     
    private void getInfoCarteira(int idPessoa, int idEstudante) throws SQLException {        
        connection = createConnection();
        Statement statement_pes = connection.createStatement();
        Statement statement_est = connection.createStatement();
        
        ResultSet dados_pes = statement_pes.executeQuery("SELECT * from pessoa where idPessoa = " + idPessoa);
        ResultSet dados_est = statement_est.executeQuery("SELECT * from estudante where idEstudante = " + idEstudante);

        dados_pes.next();
        dados_est.next();
        
        carteira_nome_field.setText(dados_pes.getString("nome"));
        carteira_instituicao_field.setText("Escola...");
        carteira_curso_field.setText(dados_est.getString("curso"));
        carteira_tipoCurso_field.setText("Ensino Básico");
        carteira_cpf_field.setText(dados_pes.getString("cpf"));
        carteira_rg_field.setText(dados_pes.getString("rg"));
        carteira_nasc_field.setText(dados_pes.getString("dataNascimento"));
        carteira_matr_field.setText(dados_est.getString("idEstudante"));
        carteira_codigo_field.setText(dados_est.getString("idEstudante"));
    }    
    

