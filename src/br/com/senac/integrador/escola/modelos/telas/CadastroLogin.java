    private void botaoCriarLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCriarLoginActionPerformed
        String usuario = campoUsuario.getText();
        String senha = campoSenha.getText();
        
        String tipoLoginIndicador = (String) comboBoxTipoLogin.getSelectedItem();
        TipoLogin tipoLogin = Enum.valueOf(TipoLogin.class, tipoLoginIndicador);
        Login login = new Login(usuario, senha, tipoLogin);
        
        try {
            SQLManager.cadastrar(login);
        } catch (SQLException ex) {
            Logger.getLogger(CadastroLogin.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        JOptionPane.showMessageDialog(null, "Login cadastrado.");
    }//GEN-LAST:event_botaoCriarLoginActionPerformed

